    /** AD -> BS. Accepts 'Y-m-d' or a DateTimeImmutable. */
    public static function adToBs(string|DateTimeImmutable $ad): array
    {
        $date = $ad instanceof DateTimeImmutable ? $ad : new DateTimeImmutable($ad);
        $reference = new DateTimeImmutable(self::REFERENCE_AD);

        $days = (int) $reference->diff($date)->format('%r%a');
        if ($days < 0) {
            throw new InvalidArgumentException('Date is before the supported BS range (2000 BS).');
        }

        $year = self::MIN_BS_YEAR;
        $month = 1;
        foreach (self::BS as $bsYear => $months) {
            foreach ($months as $i => $len) {
                if ($days < $len) {
                    return self::formatBs($bsYear, $i + 1, $days + 1);
                }
                $days -= $len;
            }
        }

        throw new InvalidArgumentException('Date is beyond the supported BS range (2090 BS).');
    }

    /** BS -> AD. Returns ['ad' => 'Y-m-d', ...]. */
    public static function bsToAd(int $year, int $month, int $day): array
    {
        if (! isset(self::BS[$year])) {
            throw new InvalidArgumentException("BS year {$year} is outside 2000–2090.");
        }
        if ($month < 1 || $month > 12) {
            throw new InvalidArgumentException('BS month must be 1–12.');
        }
        $monthLen = self::BS[$year][$month - 1];
        if ($day < 1 || $day > $monthLen) {
            throw new InvalidArgumentException("BS day must be 1–{$monthLen} for that month.");
        }

        // Total days from the BS anchor to the given BS date.
        $total = 0;
        foreach (self::BS as $bsYear => $months) {
            if ($bsYear < $year) {
                $total += array_sum($months);
            } elseif ($bsYear === $year) {
                for ($m = 0; $m < $month - 1; $m++) {
                    $total += $months[$m];
                }
                $total += $day - 1;
                break;
            }
        }

        $ad = (new DateTimeImmutable(self::REFERENCE_AD))->modify("+{$total} days");

        return [
            'ad'        => $ad->format('Y-m-d'),
            'weekday'   => $ad->format('l'),
            'bs'        => self::formatBs($year, $month, $day),
        ];
    }

    private static function formatBs(int $year, int $month, int $day): array
    {
        return [
            'year'       => $year,
            'month'      => $month,
            'day'        => $day,
            'month_name' => self::MONTHS_EN[$month],
            'formatted'  => sprintf('%04d-%02d-%02d', $year, $month, $day),
        ];
    }
